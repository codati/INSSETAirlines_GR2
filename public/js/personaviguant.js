function afficherForm(){
    
    $(document).ready(function() {
        $('#formNouveauPilote').fadeIn(300);
        $('.bglayer').show();
    });
    
}

function fermerForm(){
    
    $(document).ready(function() {
        $('#formNouveauPilote').fadeOut(300);
        $('.bglayer').hide();
    });
    
}